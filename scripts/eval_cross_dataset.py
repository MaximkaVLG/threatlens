# CIC-IDS2018 label -> CIC-IDS2017 label. Includes the well-known
# "Infilteration" typo in CIC-IDS2018.
LABEL_MAP_2018_TO_2017 = {
    "Benign": "BENIGN",
    "Bot": "Bot",
    "DDOS attack-HOIC": "DDoS",
    "DDOS attack-LOIC-UDP": "DDoS",
    "DDoS attacks-LOIC-HTTP": "DDoS",
    "DoS attacks-GoldenEye": "DoS GoldenEye",
    "DoS attacks-Hulk": "DoS Hulk",
    "DoS attacks-SlowHTTPTest": "DoS Slowhttptest",
    "DoS attacks-Slowloris": "DoS slowloris",
    "FTP-BruteForce": "FTP-Patator",
    "SSH-Bruteforce": "SSH-Patator",
    "Infilteration": "Infiltration",
    # Web-attack labels exist in 2018 too but we don't map (different naming);
    # they will surface as "OTHER" in the report.
}


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--csv", default=str(ROOT / "data" / "cicids2018" / "Thursday-01-03-2018.csv"))
    parser.add_argument("--sample", type=int, default=None,
                        help="Random subsample size (default: full file)")
    parser.add_argument("--out", default=str(OUT_JSON_DEFAULT),
                        help="Output JSON path")
    args = parser.parse_args()

    csv_path = Path(args.csv)
    print(f"[1/4] Loading {csv_path.name}")
    df = pd.read_csv(csv_path, low_memory=False)
    n0 = len(df)

    # Drop rows where Label is literal "Label" (header rows accidentally inserted)
    df = df[df["Label"] != "Label"].copy()
    print(f"  Rows: {n0:,} -> {len(df):,} after dropping corrupt header rows")

    # Map labels
    df["mapped_label"] = df["Label"].map(LABEL_MAP_2018_TO_2017).fillna("OTHER")
    print(f"  Unique mapped labels: {df['mapped_label'].value_counts().to_dict()}")

    # Drop OTHER (labels not in our trained classes)
    df = df[df["mapped_label"] != "OTHER"].copy()
    print(f"  Rows after dropping OTHER: {len(df):,}")

    if args.sample and len(df) > args.sample:
        df = df.sample(n=args.sample, random_state=42).reset_index(drop=True)
        print(f"  Subsampled to {len(df):,}")

    # Rename columns to CIC-IDS2017 names
    df = df.rename(columns=COL_MAP_2018_TO_2017)

    # Convert any remaining non-numeric values in feature columns to NaN, then fill
    feature_present = [c for c in CIC_FEATURE_COLUMNS if c in df.columns]
    feature_missing = [c for c in CIC_FEATURE_COLUMNS if c not in df.columns]
    print(f"  Feature columns: {len(feature_present)} present, {len(feature_missing)} missing")
    if feature_missing:
        # Add missing features as zeros (e.g., "Fwd Header Length.1" duplicate)
        for c in feature_missing:
            if c == "Fwd Header Length.1" and "Fwd Header Length" in df.columns:
                df[c] = df["Fwd Header Length"]
            else:
                df[c] = 0.0
        print(f"  Added missing features: {feature_missing}")

    # Cast features to numeric, replace inf/nan with 0 (matches training pipeline behavior)
    for c in CIC_FEATURE_COLUMNS:
        df[c] = pd.to_numeric(df[c], errors="coerce")
    df[CIC_FEATURE_COLUMNS] = df[CIC_FEATURE_COLUMNS].replace([np.inf, -np.inf], np.nan).fillna(0.0)

    print(f"[2/4] Loading FlowDetector")
    detector = FlowDetector.from_results_dir(str(MODELS_DIR))

    print(f"[3/4] Running inference on {len(df):,} flows")
    t0 = time.time()
    preds = detector.predict(df[CIC_FEATURE_COLUMNS])
    inference_seconds = time.time() - t0
    print(f"  Inference: {inference_seconds:.1f}s ({len(df) / inference_seconds:,.0f} flows/sec)")

    y_true = df["mapped_label"].values
    y_pred = preds["label"].values

    print(f"[4/4] Computing metrics")
    acc = accuracy_score(y_true, y_pred)
    f1_w = f1_score(y_true, y_pred, average="weighted", zero_division=0)
    f1_macro = f1_score(y_true, y_pred, average="macro", zero_division=0)

    # Binary metrics (BENIGN vs everything else)
    y_true_bin = ["BENIGN" if l == "BENIGN" else "ATTACK" for l in y_true]
    y_pred_bin = ["BENIGN" if l == "BENIGN" else "ATTACK" for l in y_pred]
    acc_bin = accuracy_score(y_true_bin, y_pred_bin)
    f1_bin = f1_score(y_true_bin, y_pred_bin, pos_label="ATTACK", average="binary", zero_division=0)
    cm_bin = confusion_matrix(y_true_bin, y_pred_bin, labels=["BENIGN", "ATTACK"]).tolist()

    # Per-class report
    report = classification_report(y_true, y_pred, output_dict=True, zero_division=0)

    # Confusion matrix on multiclass (only for classes that appear in y_true)
    classes_in_truth = sorted(set(y_true))
    cm_multi = confusion_matrix(y_true, y_pred, labels=classes_in_truth).tolist()

    # Compare per-class metrics with the in-distribution training results
    try:
        cic_metrics = json.loads((MODELS_DIR / "metrics.json").read_text())
        # metrics.json is a list of {name, accuracy, f1, ...}
        xgb_entry = next((m for m in cic_metrics if m.get("name", "").lower() == "xgboost"), None)
        in_dist_xgb_f1 = xgb_entry.get("f1") if xgb_entry else None
    except Exception:
        in_dist_xgb_f1 = None

    summary = {
        "csv": csv_path.name,
        "n_flows": int(len(df)),
        "label_counts": {str(k): int(v) for k, v in pd.Series(y_true).value_counts().items()},
        "in_dist_xgb_f1_weighted_2017": in_dist_xgb_f1,
        "cross_dist_xgb_f1_weighted_2018": round(float(f1_w), 4),
        "cross_dist_xgb_f1_macro_2018": round(float(f1_macro), 4),
        "cross_dist_accuracy": round(float(acc), 4),
        "binary_accuracy": round(float(acc_bin), 4),
        "binary_f1_attack": round(float(f1_bin), 4),
        "binary_confusion_matrix": {
            "labels": ["BENIGN", "ATTACK"],
            "matrix": cm_bin,
            "format": "rows=true, cols=predicted",
        },
        "multiclass_confusion_matrix_truth_classes": {
            "labels": classes_in_truth,
            "all_pred_labels": list(set(y_pred)),
            "matrix": cm_multi,
        },
        "classification_report": report,
        "inference_seconds": round(inference_seconds, 2),
    }

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(summary, indent=2))
    print(f"\nWritten: {out_path}")
    # Print abbreviated summary
    print(f"\nIn-distribution F1 weighted (CIC-IDS2017 test split): {in_dist_xgb_f1}")
    print(f"Cross-dataset F1 weighted (CIC-IDS2018):              {f1_w:.4f}")
    print(f"Cross-dataset F1 macro:                               {f1_macro:.4f}")
    print(f"Cross-dataset accuracy:                               {acc:.4f}")
    print(f"Binary accuracy (BENIGN vs ATTACK):                   {acc_bin:.4f}")
    print(f"Binary F1 (ATTACK class):                             {f1_bin:.4f}")
    print(f"\nBinary confusion matrix (rows=true, cols=predicted):")
    print(f"                BENIGN(pred)  ATTACK(pred)")
    print(f"  BENIGN(true)  {cm_bin[0][0]:>11,}  {cm_bin[0][1]:>11,}")
    print(f"  ATTACK(true)  {cm_bin[1][0]:>11,}  {cm_bin[1][1]:>11,}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
