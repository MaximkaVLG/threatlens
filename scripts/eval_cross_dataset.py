"""Cross-dataset evaluation: train on CIC-IDS2017, test on CIC-IDS2018.

CIC-IDS2018 is generated by the same CICFlowMeter family as CIC-IDS2017 but
uses abbreviated column names (Tot Fwd Pkts vs Total Fwd Packets) and a
slightly different label vocabulary (Benign vs BENIGN, Infilteration typo).
This script handles the mapping, runs inference with the existing trained
XGBoost model, and reports per-class metrics.

Usage:
    python scripts/eval_cross_dataset.py [--csv data/cicids2018/Thursday-01-03-2018.csv]
"""
from __future__ import annotations

