import json
import sys
import time
from pathlib import Path

import numpy as np
import pandas as pd
from sklearn.metrics import (
    accuracy_score,
    classification_report,
    confusion_matrix,
    f1_score,
)

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

from threatlens.network.detector import FlowDetector  # noqa: E402
from threatlens.network.flow_extractor import CIC_FEATURE_COLUMNS  # noqa: E402

MODELS_DIR = ROOT / "results" / "cicids2017"
OUT_JSON_DEFAULT = ROOT / "results" / "cross_dataset_eval.json"

